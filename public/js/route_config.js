angular.module("Application")
    .config(function ($routeSegmentProvider,$routeProvider) {
        $routeSegmentProvider
            .when('/main','main.home')
            .when('/managment','managment')
            .when('/welcome','welcome')
            .when('/main/edit_profile','main.edit_profile')
            .when('/main/view_profile/:id','main.view_profile')
            .when('/main/edit_profile_photo','main.edit_profile_photo')
            .when('/main/edit_photo','main.edit_photo')
            .when('/main/add_photo','main.add_photo');
        $routeSegmentProvider.segment('welcome',{
            templateUrl:'../templates/welcome.html',
            controller:'RegCtrl'
        });
        $routeSegmentProvider.segment('main',{
            templateUrl:'../templates/main.html',
            controller:'UserCtrl'
        });
        $routeSegmentProvider.segment('managment',{
           templateUrl:'../templates/user_managment.html',
            controller:'AdminCtrl'
        });
        $routeSegmentProvider.within('main').segment('home',{
            templateUrl:'../templates/photo_conteiner_tmp.html'
        });
        $routeSegmentProvider.within('main').segment('edit_profile',{
            templateUrl:'../templates/edit_profile_tmp.html'
        });
        $routeSegmentProvider.within('main').segment('view_profile',{
            templateUrl:'../templates/view_profile_tmp.html'
        });
        $routeSegmentProvider.within('main').segment('edit_profile_photo',{
            templateUrl:'../templates/photo_profile.html'
        });
        $routeSegmentProvider.within('main').segment('edit_photo',{
            templateUrl:'../templates/photo_profile.html'
        });
        $routeSegmentProvider.within('main').segment('add_photo',{
            templateUrl:'../templates/photo_change_tmp.html'
        });
        $routeProvider.otherwise({redirectTo:'/main'});
    });
